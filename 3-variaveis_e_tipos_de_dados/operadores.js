//Operações aritméticas

var a = 100;
var b = 50;
var resultado = a + b; //soma

console.log(a + " + " + b + " = " + resultado);

resultado = a - b; //subtração

console.log(a + " - " + b + " = " + resultado);

resultado = a * b; //subtração

console.log(a + " x " + b + " = " + resultado);

resultado = a / b; //subtração

console.log(a + " / " + b + " = " + resultado);

var conta = 5 + 5 * 10; //a multiplicação será feita primeiro

console.log(conta);

conta = (5 + 5) * 10; //a soma será feita primeiro

console.log(conta);

